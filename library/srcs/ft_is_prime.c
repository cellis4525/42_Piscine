#include <unistd.h>

int	ft_is_prime(int nb)
{
	int	d;

	d = 2;
	if (nb == 0 || nb == 1 || nb < 0)
	{
		return (0);
	}
	while (d <= nb / 2)
	{
		if (nb % d == 0)
		{
			return (0);
		}
		d++;
	}
	return (1);
}
