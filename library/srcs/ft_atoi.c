int	ft_atoi(char *str)
{
	int o;
	int s;
	int i;

	o = 0;
	s = 1;
	i = 0;
	if (str[i] == '-')
	{
		s = -1;
		i++;
	}
	if (!(str[i] >= '0' && str[i] <= '9'))
		return (0);
	while (str[i] != '\0')
	{
		if (str[i] < '0' || str[i] > '9')
			break ;
		o = o * 10 + (str[i] - '0');
		i++;
	}
	return (s * o);
}
