#include <unistd.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb == 0 || nb == 1)
	{
		return (nb);
	}
	while (i * i != nb && i * i != 1 / nb)
	{
		if (i * 2 > nb && i * i < 1 / nb)
		{
			return (0);
		}
		i++;
	}
	return (i);
}
