#include <unistd.h>

int	ft_iterative_power(int nb, int power)
{
	int	ttl;

	ttl = 1;
	if (power == 0)
	{
		return (1);
	}
	if (power < 0)
	{
		return (0);
	}
	while (power > 0)
	{
		ttl = ttl * nb;
		power--;
	}
	return (ttl);
}
